class User:
    #constructor

    def __init__(self, email):
        self.email = email

    #setters and getters
    def getEmail(self):
        return self.email
        
    def setFName(self, fName):
        self.fname = fName

    def getFName(self):
        return self.fname

    def setLName(self, lName):
        self.lname = lName

    def getLName(self):
        return self.lname

    def setPassword(self, passwd):
        self.password = passwd

    def getPassword(self):
        return self.password

    def setBDate(self, bDate):
        self.bdate = bDate

    def getBDate(self):
        return self.bdate

    def setCountry(self, country):
        self.country = country

    def getCountry(self):
        return self.country

    def setCity(self, city):
        self.city = city

    def getCity(self):
        return self.city

    def setJob(self, job):
        self.job = job

    def getJob(self):
        return self.job

    def setCash(self, cash):
        self.cash = cash

    def getCash(self):
        return self.cash

